# Escreva um programa que leia 6 notas diferentes e faça a média do aluno

# Entrada de dados
n1 = float(input('Digite a primeira nota: '))
n2 = float(input('Digite a segunda nota: '))
n3 = float(input('Digite a terceira nota: '))
n4 = float(input('Digite a quarta nota: '))
n5 = float(input('Digite a quinta nota: '))
n6 = float(input('Digite a sexta nota: '))

# Cálculo interno
media_notas = (n1 + n2 + n3 + n4 + n5 + n6) / 6

# Saída de dados
print(f'A sua média final é: {media_notas}')